// exercise 10: Menu Switch

// Store the menu choice 
let choice = 2;

switch (choice) {
    case 1:
    console.log("Burger");
    break;

    case 2:
        console.log("Pizza");
        break;

    case 3:
        console.log("Pasta");
        break;
        
    case 4:
        console.log("Salad");
        break;
    
        default:
            console.log("Invalid choice");
}